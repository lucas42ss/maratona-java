package academy.devdojo.maratonajava.introducao;

public class Aula02TiposPrimitivos {
    public static void  main(String[] arg){
        //8 - tipos primitivos -> int, double, float, char, byte, short, long, boolean
        //tamanho -> 1 bit
        boolean cadastrado = true;
        //tamanho -> 1 byte
        byte idadeByte = 10;
        //tamanho -> 2 bytes
        short idadeShort = 25000;
        //tamanho -> 2 bytes
        char letra = 'l';
        //tamanho -> 4 bytes
        int age = 25;
        //tamanho -> 4 bytes
        float salarioFLoat = 2500.55F;
        //tamanho -> 8 bytes
        long distancia = 100000L;
        //tamanho -> 8 bytes
        double altura = 1.80D;
    }
}
